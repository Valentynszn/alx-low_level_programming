#include "main.h"

/**
 * add - Adds two integers.
 *
 * Description: This function takes two integers,
 * 'n1' and 'n2', and returns their sum.
 *
 * @n1: The first integer.
 * @n2: The second integer.
 *
 * Return: The sum of 'n1' and 'n2'.
 */


int add(int n1, int n2)
{
	int sum;

	sum = n1 + n2;
	return (sum);
}
